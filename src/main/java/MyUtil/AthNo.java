package MyUtil;

public class AthNo {
    private int athid;
    private String no;

    public AthNo() {
    }

    public AthNo(int athid, String no) {
        this.athid = athid;
        this.no = no;
    }

    public int getAthid() {
        return athid;
    }

    public void setAthid(int athid) {
        this.athid = athid;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
